class CALCULADORA {


    fun soma(n1: Int, n2: Int): Int{

        return n1 + n2
    }
    fun subtracao(n1: Int, n2: Int): Int{

        return n1 - n2
    }
    fun divisao(n1: Int, n2: Int): Int{

        return n1 / n2
    }
    fun multiplicacao (n1: Int, n2: Int): Int{

        return n1 * n2
    }

}